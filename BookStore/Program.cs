using BookStore.Data;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
//builder.Services.AddDbContext<BookAPIDbContext>(options => options.UseInMemoryDatabase("BooksDb")); //Statement causes Injection Of Database
builder.Services.AddDbContext<BookAPIDbContext>(options => options.UseSqlServer(builder.Configuration.GetConnectionString("BookApiConnectionString"))); //Statement causes Connection With Sql Server Database 
var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
